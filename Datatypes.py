#What is Python?

#Python is a high-level, interpreted programming language that is widely used for various purposes such as web development, scientific computing, data analysis, artificial intelligence, and more.

#What is a Variable in Python?

#In Python, a variable is a name given to a value. Variables are used to store and manipulate data in a program.

# Declaring Variables in Python

# In Python, you don't need to declare variables before using them. You can assign a value to a variable using the assignment operator (=). For example:


# x = 5  # assigns the value 5 to the variable x
# y = "Hello"  # assigns the string "Hello" to the variable y

# Variable Naming Conventions in Python

# Here are some variable naming conventions in Python:

# 1. Use lowercase letters: Variable names should start with a lowercase letter.
# 2. Use underscores: Use underscores to separate words in a variable name.
# 3. Avoid special characters: Avoid using special characters like !, @, #, $, etc. in variable names.
# 4. Be descriptive: Choose variable names that are descriptive and indicate the purpose of the variable.

# Data Types of Variables in Python

# Python has several built-in data types that can be assigned to variables:

# 1. Integers: int
# 2. Floats: float
# 3. Strings: str
# 4. Boolean: bool
# 5. List: list
# 6. Tuple: tuple
# 7. Dictionary: dict
# 8. Set: set



#Numeric Data Types

#1. Integers (int): whole numbers, either positive, negative, or zero.

x = 5
print(x)  # Output: 5


#2. Floats (float): decimal numbers, either positive, negative, or zero.

x = 3.14
print(x)  # Output: 3.14


#3. Complex Numbers (complex): numbers with real and imaginary parts.

x = 3 + 5j
print(x)  # Output: (3+5j)


#Text Data Types

#1. Strings (str): sequences of characters, such as words or sentences.

x = "Hello, World!"
print(x)  # Output: Hello, World!


#Sequence Data Types

#1. Lists (list): ordered collections of items that can be of any data type.

x = [1, 2, 3, "four", 5.6]
print(x)  # Output: [1, 2, 3, 'four', 5.6]


#2. Tuples (tuple): ordered, immutable collections of items that can be of any data type.

x = (1, 2, 3, "four", 5.6)
print(x)  # Output: (1, 2, 3, 'four', 5.6)


#Mapping Data Types

#1. Dictionaries (dict): unordered collections of key-value pairs.

x = {"name": "John", "age": 30, "city": "New York"}
print(x)  # Output: {'name': 'John', 'age': 30, 'city': 'New York'}


#Set Data Types

#1. Sets (set): unordered collections of unique items.

x = {1, 2, 3, 4, 5}
print(x)  # Output: {1, 2, 3, 4, 5}


#2. Frozensets (frozenset): immutable sets.

x = frozenset([1, 2, 3, 4, 5])
print(x)  # Output: frozenset({1, 2, 3, 4, 5})


#Boolean Data Type

#1. Boolean (bool): a logical value that can be either True or False.

x = True
print(x)  # Output: True

